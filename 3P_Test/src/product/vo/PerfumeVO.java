package product.vo;

public class PerfumeVO {
	private int		perSq;
	private int 	bdSq;
	private String 	perName;
	private int		perPrice;
	private String	perShort;
	private String	perLong;
	private String 	perDate;
	
	public int getPerSq() {
		return perSq;
	}
	public void setPerSq(int perSq) {
		this.perSq = perSq;
	}
	public int getBdSq() {
		return bdSq;
	}
	public void setBdSq(int bdSq) {
		this.bdSq = bdSq;
	}
	public String getPerName() {
		return perName;
	}
	public void setPerName(String perName) {
		this.perName = perName;
	}
	public int getPerPrice() {
		return perPrice;
	}
	public void setPerPrice(int perPrice) {
		this.perPrice = perPrice;
	}
	public String getPerShort() {
		return perShort;
	}
	public void setPerShort(String perShort) {
		this.perShort = perShort;
	}
	public String getPerLong() {
		return perLong;
	}
	public void setPerLong(String perLong) {
		this.perLong = perLong;
	}
	public String getPerDate() {
		return perDate;
	}
	public void setPerDate(String perDate) {
		this.perDate = perDate;
	}
}